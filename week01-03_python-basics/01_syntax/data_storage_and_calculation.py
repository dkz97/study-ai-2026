# 数据的存储与运算

"""
字面量与变量
"""
# 字面量：程序中直接书写的固定值，就成为字面量 (类似于java中的常量)
# 数字类型： 整数(int)、浮点数(float)、复数(complex)
print(1); print(1.1); print(1+1j) # 复数 实部和虚
# 字符串类型： 字符串(str)
print("Hello, World!")
# 布尔类型： 布尔(bool)
print(True); print(False) # 注意 bool本质是int类型，True对应1，False对应0，可以进行数学运算
# 空值类型： 空值(NoneType)
print(None)
# 数据容器： 列表(list)、元组(tuple)、集合(set)、字典(dict)


# 变量：程序中用来存储数据的内存空间，相当于容器 (变量名=变量值)
# python的变量是动态类型，变量名可以指向不同的数据类型
num = 10; print(num)
num = "Hello, World!"; print(num)
num, name = 10, "dkz97" # 多变量赋值


# 标识符：程序中用来标识变量、函数、类、模块等的名称
# 命名规则：只能包含字母、数字、下划线，不能以数字开头，不能包含空格，不能包含特殊字符，不能包含关键字
# 命名规范：驼峰命名法、下划线命名法
# 驼峰命名法：首字母小写，每个单词的首字母大写
# 下划线命名法：每个单词都用下划线连接
# 例如：my_name, myName, myName2

# python的变量赋值交换可以使用多重赋值进行交换
# 流程： 右侧表达式相当于一个元祖，然后再依次赋值给左侧
# 1、c , b, a 转为元组 temp = (30, 20, 10)
# 2、左侧开始赋值等于 a = temp[0], b = temp[1], c = temp[2]
a , b , c = 10, 20, 30
a , b , c = c , b , a
print(a, b, c)


"""常见数据类型"""
# 基本数据类型
# 整数(int); 浮点数(float); 复数(complex); 字符串(str); 布尔(bool); 空值(NoneType)
# 序列类型
# 列表(list)可存多个数据，可修改，有序可重复; 元祖(tuple)和列表类似，不可修改; 字符串(str)不可变，有序不可重复
# 集合类型
# 集合(set)无序不重复; 
# 映射类型
# 字典(dict)键值对，键唯一，值可重复; 

# type() 函数可以查看变量类型; isinstance() 函数可以判断变量类型; 
print(type(10))
print(isinstance(10, int))

