# 运算符
# 算术运算符
# 加法(+) 减法(-) 乘法(*) 除法(/) 整除(//) 取余/求模(%) 幂指数(**)
print(1+1)
print(1-1)
print(1*2)
print(10/3)
print(10//3)
print(10%3)
print(2**3)

# 赋值运算符
# 其实除了 (=) 其他都类似于算术缩写，如 += -= *= /= %= //= **=
num = 1
num *= 3
print(num)

# 比较运算符
# 用于比较两个值的关系，返回bool值
# 等于(==) 不等于(!=) 大于(>) 大于等于(>=) 小于(<) 小于等于(<=)

# 逻辑运算符
# 用于连接多个条件(bool)表达式，并且最终返回一个bool结果的运算符
# and(逻辑与/并且) or(逻辑或/或者) not(逻辑反/取反)
bool1 = True
bool2 = False
print(bool1 and bool2)
print(bool1 or bool2)
print(not bool1)
